def interpolate(benchmarks):
    """Interpolate data for all years 2000–2025 from benchmark data."""
    years = list(range(2000, 2026))
    result = {}
    num_indicators = 9  # gdp, growth, gdp_pc, pop, inf, unemp, fdi, debt, trade

    for year in years:
        result[year] = []
        for i in range(num_indicators):
            # Find surrounding benchmarks
            bench_years = sorted(benchmarks.keys())
            if year in benchmarks:
                result[year].append(benchmarks[year][i])
            elif year < min(bench_years):
                result[year].append(benchmarks[min(bench_years)][i])
            elif year > max(bench_years):
                result[year].append(benchmarks[max(bench_years)][i])
            else:
                # Linear interpolation between nearest benchmarks
                lower = max(y for y in bench_years if y < year)
                upper = min(y for y in bench_years if y > year)
                low_val = benchmarks[lower][i]
                up_val = benchmarks[upper][i]
                frac = (year - lower) / (upper - lower)
                result[year].append(low_val + frac * (up_val - low_val))

    return result


def build_dataset():
    """Build the complete dataset."""
    data = {"countries": [], "years": list(range(2000, 2026))}

    for c in COUNTRIES:
        country_id = c["id"]
        if country_id not in BENCHMARKS:
            print(f"WARNING: No benchmark data for {c['name']} ({country_id}) — skipping")
            continue

        all_years = interpolate(BENCHMARKS[country_id])

        # Build time series
        gdp = []
        growth = []
        gdp_per_capita = []
        population = []
        inflation = []
        unemployment = []
        fdi = []
        debt = []
        trade_balance = []

        for year in range(2000, 2026):
            vals = all_years[year]
            gdp.append(round(vals[0], 2))
            growth.append(round(vals[1], 2))
            gdp_per_capita.append(round(vals[2], 0))
            population.append(round(vals[3], 2))
            inflation.append(round(vals[4], 2))
            unemployment.append(round(vals[5], 1))
            fdi.append(round(vals[6], 2))
            debt.append(round(vals[7], 1))
            trade_balance.append(round(vals[8], 2))

        entry = {
            "id": c["id"],
            "name": c["name"],
            "lat": c["lat"],
            "lon": c["lon"],
            "iso_a3": c["iso_a3"],
            "region": c["region"],
            "eu": c["eu"],
            "euro": c["euro"],
            "flag": c["flag"],
            "gdp": gdp,
            "growth": growth,
            "gdpPerCapita": gdp_per_capita,
            "population": population,
            "inflation": inflation,
            "unemployment": unemployment,
            "fdi": fdi,
            "debt": debt,
            "tradeBalance": trade_balance,
        }
        data["countries"].append(entry)

    return data


